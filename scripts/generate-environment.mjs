import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = join(dirname(fileURLToPath(import.meta.url)), '..');
const isDevelopment = process.argv.includes('--development');

const loadEnvFile = (fileName) => {
  const filePath = join(rootDir, fileName);
  if (!existsSync(filePath)) return;

  const lines = readFileSync(filePath, 'utf8').split(/\r?\n/);
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#') || !trimmed.includes('=')) continue;

    const [rawKey, ...rawValue] = trimmed.split('=');
    const key = rawKey.trim();
    const value = rawValue.join('=').trim().replace(/^['"]|['"]$/g, '');

    if (key && process.env[key] === undefined) {
      process.env[key] = value;
    }
  }
};

loadEnvFile('.env');
loadEnvFile('.env.local');

const readEnv = (key, fallback = '') => process.env[key] ?? fallback;

const firebaseConfig = {
  apiKey: readEnv('PLANGO_FIREBASE_API_KEY'),
  authDomain: readEnv('PLANGO_FIREBASE_AUTH_DOMAIN'),
  projectId: readEnv('PLANGO_FIREBASE_PROJECT_ID'),
  storageBucket: readEnv('PLANGO_FIREBASE_STORAGE_BUCKET'),
  messagingSenderId: readEnv('PLANGO_FIREBASE_MESSAGING_SENDER_ID'),
  appId: readEnv('PLANGO_FIREBASE_APP_ID'),
  measurementId: readEnv('PLANGO_FIREBASE_MEASUREMENT_ID'),
};

const baseEnvironment = {
  apiUrl: readEnv('PLANGO_API_URL', 'https://pplango.me/api'),
  mapTilerKey: readEnv('PLANGO_MAPTILER_KEY'),
  nominatimUrl: readEnv('PLANGO_NOMINATIM_URL', 'https://nominatim.openstreetmap.org'),
  firebase: firebaseConfig,
  firebaseVapidKey: readEnv('PLANGO_FIREBASE_VAPID_KEY'),
};

const environmentFile = (production) => `export const environment = ${JSON.stringify(
  {
    production,
    ...baseEnvironment,
  },
  null,
  2,
)} as const;
`;

const serviceWorkerFile = `/* Generated by scripts/generate-environment.mjs. Do not edit directly. */
importScripts('https://www.gstatic.com/firebasejs/12.14.0/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/12.14.0/firebase-messaging-compat.js');

firebase.initializeApp(${JSON.stringify(firebaseConfig, null, 2)});

const messaging = firebase.messaging();
const CACHE_NAME = 'plango-shell-v1';
const APP_SHELL = [
  '/',
  '/manifest.webmanifest',
  '/favicon.svg',
  '/favicon-48x48.png',
  '/favicon-96x96.png',
  '/apple-touch-icon.png',
  '/assets/seo/logo-512.png',
];
const DEFAULT_ICON = '/assets/seo/logo-512.png';
const DEFAULT_BADGE = '/favicon-96x96.png';

const ROUTE_MAP = {
  task_deadline: '/user/tasks',
  appointment_preparation: '/user/calendar',
  appointment_departure: '/user/calendar',
  new_follower: '/user/network',
  new_follow_request: '/user/network',
  follow_request_accepted: '/user/network',
};

messaging.onBackgroundMessage((payload) => {
  const notificationData = payload.notification || {};
  const dataPayload = payload.data || {};

  self.clients
    .matchAll({ type: 'window', includeUncontrolled: true })
    .then((windowClients) => {
      windowClients.forEach((client) => {
        client.postMessage({
          type: 'PLANGO_NOTIFICATION_RECEIVED',
          payload,
        });
      });
    });

  if (!notificationData.title && dataPayload.title) {
    self.registration.showNotification(dataPayload.title, {
      body: dataPayload.body || '',
      icon: dataPayload.icon || DEFAULT_ICON,
      badge: DEFAULT_BADGE,
      tag: dataPayload.type || 'plango-notification',
      data: dataPayload,
      vibrate: [200, 100, 200],
      requireInteraction: dataPayload.requireInteraction === 'true',
    });
  }
});

self.addEventListener('notificationclick', (event) => {
  event.notification.close();

  const messageData = event.notification.data?.FCM_MSG?.data || event.notification.data || {};
  const targetPath = messageData.link || ROUTE_MAP[messageData.type] || '/user/notifications';
  const targetUrl = new URL(targetPath, self.location.origin).href;

  event.waitUntil(
    self.clients.matchAll({ type: 'window', includeUncontrolled: true }).then((windowClients) => {
      const existingClient = windowClients.find((client) =>
        client.url.startsWith(self.location.origin),
      );

      if (existingClient) {
        existingClient.navigate(targetUrl);
        return existingClient.focus();
      }

      return self.clients.openWindow(targetUrl);
    }),
  );
});

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches
      .open(CACHE_NAME)
      .then((cache) => cache.addAll(APP_SHELL))
      .catch(() => undefined)
      .then(() => self.skipWaiting()),
  );
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((cacheNames) =>
        Promise.all(
          cacheNames
            .filter((cacheName) => cacheName !== CACHE_NAME)
            .map((cacheName) => caches.delete(cacheName)),
        ),
      )
      .then(() => self.clients.claim()),
  );
});

self.addEventListener('fetch', (event) => {
  const { request } = event;

  if (request.method !== 'GET') return;

  const requestUrl = new URL(request.url);
  if (requestUrl.origin !== self.location.origin) return;
  if (requestUrl.pathname.startsWith('/api/')) return;

  if (request.mode === 'navigate') {
    event.respondWith(fetch(request).catch(() => caches.match('/')));
    return;
  }

  event.respondWith(
    caches.match(request).then((cachedResponse) => {
      if (cachedResponse) return cachedResponse;

      return fetch(request).then((networkResponse) => {
        if (!networkResponse || !networkResponse.ok) return networkResponse;

        const responseToCache = networkResponse.clone();
        caches.open(CACHE_NAME).then((cache) => cache.put(request, responseToCache));
        return networkResponse;
      });
    }),
  );
});
`;

mkdirSync(join(rootDir, 'src', 'environments'), { recursive: true });
writeFileSync(
  join(rootDir, 'src', 'environments', 'environment.ts'),
  environmentFile(!isDevelopment),
);
writeFileSync(
  join(rootDir, 'src', 'environments', 'environment.development.ts'),
  environmentFile(false),
);

mkdirSync(join(rootDir, 'public'), { recursive: true });
writeFileSync(join(rootDir, 'public', 'firebase-messaging-sw.js'), serviceWorkerFile);

console.log(
  `[env] Generated Angular environment files and Firebase service worker for ${
    isDevelopment ? 'development' : 'production'
  }.`,
);
